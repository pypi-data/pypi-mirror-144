def r2_score_dist_helper(y_true, y_pred, sample_weight, multioutput):
    rpgm__mqek = MPI.COMM_WORLD
    if y_true.ndim == 1:
        y_true = y_true.reshape((-1, 1))
    if y_pred.ndim == 1:
        y_pred = y_pred.reshape((-1, 1))
    if not check_consistent_length_parallel(y_true, y_pred, sample_weight):
        raise ValueError(
            'y_true, y_pred and sample_weight (if not None) have inconsistent number of samples'
            )
    hmt__lmlf = y_true.shape[0]
    oozlj__vfo = rpgm__mqek.allreduce(hmt__lmlf, op=MPI.SUM)
    if oozlj__vfo < 2:
        warnings.warn(
            'R^2 score is not well-defined with less than two samples.',
            UndefinedMetricWarning)
        return np.array([float('nan')])
    if sample_weight is not None:
        sample_weight = column_or_1d(sample_weight)
        tcfyh__nucnq = sample_weight[:, np.newaxis]
    else:
        sample_weight = np.float64(y_true.shape[0])
        tcfyh__nucnq = 1.0
    vvuvx__boj = (tcfyh__nucnq * (y_true - y_pred) ** 2).sum(axis=0, dtype=
        np.float64)
    ntht__iwcw = np.zeros(vvuvx__boj.shape, dtype=vvuvx__boj.dtype)
    rpgm__mqek.Allreduce(vvuvx__boj, ntht__iwcw, op=MPI.SUM)
    oury__ebv = np.nansum(y_true * tcfyh__nucnq, axis=0, dtype=np.float64)
    scql__vuthf = np.zeros_like(oury__ebv)
    rpgm__mqek.Allreduce(oury__ebv, scql__vuthf, op=MPI.SUM)
    gvn__vhq = np.nansum(sample_weight, dtype=np.float64)
    dggks__uan = rpgm__mqek.allreduce(gvn__vhq, op=MPI.SUM)
    ehne__xok = scql__vuthf / dggks__uan
    aozxu__grjt = (tcfyh__nucnq * (y_true - ehne__xok) ** 2).sum(axis=0,
        dtype=np.float64)
    pyf__ehznu = np.zeros(aozxu__grjt.shape, dtype=aozxu__grjt.dtype)
    rpgm__mqek.Allreduce(aozxu__grjt, pyf__ehznu, op=MPI.SUM)
    dys__mlr = pyf__ehznu != 0
    hhz__lmbz = ntht__iwcw != 0
    vethi__voy = dys__mlr & hhz__lmbz
    tnhf__fcop = np.ones([y_true.shape[1] if len(y_true.shape) > 1 else 1])
    tnhf__fcop[vethi__voy] = 1 - ntht__iwcw[vethi__voy] / pyf__ehznu[vethi__voy
        ]
    tnhf__fcop[hhz__lmbz & ~dys__mlr] = 0.0
    if isinstance(multioutput, str):
        if multioutput == 'raw_values':
            return tnhf__fcop
        elif multioutput == 'uniform_average':
            mdrqj__qczfq = None
        elif multioutput == 'variance_weighted':
            mdrqj__qczfq = pyf__ehznu
            if not np.any(dys__mlr):
                if not np.any(hhz__lmbz):
                    return np.array([1.0])
                else:
                    return np.array([0.0])
    else:
        mdrqj__qczfq = multioutput
    return np.array([np.average(tnhf__fcop, weights=mdrqj__qczfq)])


