def fit_sgd(m, X, y, y_classes=None, _is_data_distributed=False):
    rpgm__mqek = MPI.COMM_WORLD
    bqrl__vxyd = rpgm__mqek.allreduce(len(X), op=MPI.SUM)
    gxz__pediy = len(X) / bqrl__vxyd
    ufk__kkn = rpgm__mqek.Get_size()
    m.n_jobs = 1
    m.early_stopping = False
    frs__rqpo = np.inf
    lkzo__vaf = 0
    if m.loss == 'hinge':
        tfn__nvrli = hinge_loss
    elif m.loss == 'log':
        tfn__nvrli = log_loss
    elif m.loss == 'squared_error':
        tfn__nvrli = mean_squared_error
    else:
        raise ValueError('loss {} not supported'.format(m.loss))
    rll__xrv = False
    if isinstance(m, sklearn.linear_model.SGDRegressor):
        rll__xrv = True
    for lhyf__zohgs in range(m.max_iter):
        if rll__xrv:
            m.partial_fit(X, y)
        else:
            m.partial_fit(X, y, classes=y_classes)
        m.coef_ = m.coef_ * gxz__pediy
        m.coef_ = rpgm__mqek.allreduce(m.coef_, op=MPI.SUM)
        m.intercept_ = m.intercept_ * gxz__pediy
        m.intercept_ = rpgm__mqek.allreduce(m.intercept_, op=MPI.SUM)
        if rll__xrv:
            y_pred = m.predict(X)
            hrci__coo = tfn__nvrli(y, y_pred)
        else:
            y_pred = m.decision_function(X)
            hrci__coo = tfn__nvrli(y, y_pred, labels=y_classes)
        doc__ipadm = rpgm__mqek.allreduce(hrci__coo, op=MPI.SUM)
        hrci__coo = doc__ipadm / ufk__kkn
        if m.tol > np.NINF and hrci__coo > frs__rqpo - m.tol * bqrl__vxyd:
            lkzo__vaf += 1
        else:
            lkzo__vaf = 0
        if hrci__coo < frs__rqpo:
            frs__rqpo = hrci__coo
        if lkzo__vaf >= m.n_iter_no_change:
            break
    return m


