def mse_mae_dist_helper(y_true, y_pred, sample_weight, multioutput, squared,
    metric):
    if metric == 'mse':
        nid__ibs = sklearn.metrics.mean_squared_error(y_true, y_pred,
            sample_weight=sample_weight, multioutput='raw_values', squared=True
            )
    elif metric == 'mae':
        nid__ibs = sklearn.metrics.mean_absolute_error(y_true, y_pred,
            sample_weight=sample_weight, multioutput='raw_values')
    else:
        raise RuntimeError(
            f"Unrecognized metric {metric}. Must be one of 'mae' and 'mse'")
    rpgm__mqek = MPI.COMM_WORLD
    pnmdu__soh = rpgm__mqek.Get_size()
    if sample_weight is not None:
        osdp__ucg = np.sum(sample_weight)
    else:
        osdp__ucg = np.float64(y_true.shape[0])
    qpk__rnvn = np.zeros(pnmdu__soh, dtype=type(osdp__ucg))
    rpgm__mqek.Allgather(osdp__ucg, qpk__rnvn)
    yqti__pxjx = np.zeros((pnmdu__soh, *nid__ibs.shape), dtype=nid__ibs.dtype)
    rpgm__mqek.Allgather(nid__ibs, yqti__pxjx)
    jrbor__wpk = np.average(yqti__pxjx, weights=qpk__rnvn, axis=0)
    if metric == 'mse' and not squared:
        jrbor__wpk = np.sqrt(jrbor__wpk)
    if isinstance(multioutput, str) and multioutput == 'raw_values':
        return jrbor__wpk
    elif isinstance(multioutput, str) and multioutput == 'uniform_average':
        return np.average(jrbor__wpk)
    else:
        return np.average(jrbor__wpk, weights=multioutput)


