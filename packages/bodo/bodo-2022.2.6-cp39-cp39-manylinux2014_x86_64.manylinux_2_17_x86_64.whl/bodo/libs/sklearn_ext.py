@numba.njit
def precision_recall_fscore_parallel(y_true, y_pred, operation, average=
    'binary'):
    labels = bodo.libs.array_kernels.unique(y_true, parallel=True)
    labels = bodo.allgatherv(labels, False)
    labels = bodo.libs.array_kernels.sort(labels, ascending=True, inplace=False
        )
    mwqo__kuz = len(labels)
    jzya__wllm = np.zeros(mwqo__kuz, np.int64)
    gxyrb__yls = np.zeros(mwqo__kuz, np.int64)
    hok__hlx = np.zeros(mwqo__kuz, np.int64)
    hlut__teyq = (bodo.hiframes.pd_categorical_ext.
        get_label_dict_from_categories(labels))
    for bfdx__zml in range(len(y_true)):
        gxyrb__yls[hlut__teyq[y_true[bfdx__zml]]] += 1
        if y_pred[bfdx__zml] not in hlut__teyq:
            continue
        skvg__jrfo = hlut__teyq[y_pred[bfdx__zml]]
        hok__hlx[skvg__jrfo] += 1
        if y_true[bfdx__zml] == y_pred[bfdx__zml]:
            jzya__wllm[skvg__jrfo] += 1
    jzya__wllm = bodo.libs.distributed_api.dist_reduce(jzya__wllm, np.int32
        (Reduce_Type.Sum.value))
    gxyrb__yls = bodo.libs.distributed_api.dist_reduce(gxyrb__yls, np.int32
        (Reduce_Type.Sum.value))
    hok__hlx = bodo.libs.distributed_api.dist_reduce(hok__hlx, np.int32(
        Reduce_Type.Sum.value))
    ysaoz__qnt = hok__hlx - jzya__wllm
    yszp__diu = gxyrb__yls - jzya__wllm
    pyqbe__wzndc = jzya__wllm
    per__ntmqg = y_true.shape[0] - pyqbe__wzndc - ysaoz__qnt - yszp__diu
    with numba.objmode(result='float64[:]'):
        MCM = np.array([per__ntmqg, ysaoz__qnt, yszp__diu, pyqbe__wzndc]
            ).T.reshape(-1, 2, 2)
        if operation == 'precision':
            result = precision_recall_fscore_support_helper(MCM, average)[0]
        elif operation == 'recall':
            result = precision_recall_fscore_support_helper(MCM, average)[1]
        elif operation == 'f1':
            result = precision_recall_fscore_support_helper(MCM, average)[2]
        if average is not None:
            result = np.array([result])
    return result


