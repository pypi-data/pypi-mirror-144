def confusion_matrix_dist_helper(y_true, y_pred, labels=None, sample_weight
    =None, normalize=None):
    if normalize not in ['true', 'pred', 'all', None]:
        raise ValueError(
            "normalize must be one of {'true', 'pred', 'all', None}")
    rpgm__mqek = MPI.COMM_WORLD
    try:
        ied__vkm = sklearn.metrics.confusion_matrix(y_true, y_pred, labels=
            labels, sample_weight=sample_weight, normalize=None)
    except ValueError as vbymf__ghyrp:
        ied__vkm = vbymf__ghyrp
    reycy__mfx = isinstance(ied__vkm, ValueError
        ) and 'At least one label specified must be in y_true' in ied__vkm.args[
        0]
