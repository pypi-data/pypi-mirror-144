def sklearn_preprocessing_standard_scaler_fit_dist_helper(m, X):
    rpgm__mqek = MPI.COMM_WORLD
    pnmdu__soh = rpgm__mqek.Get_size()
    ocliw__yzxhd = m.with_std
    yfozi__oxq = m.with_mean
    m.with_std = False
    if ocliw__yzxhd:
        m.with_mean = True
    m = m.fit(X)
    m.with_std = ocliw__yzxhd
    m.with_mean = yfozi__oxq
    if not isinstance(m.n_samples_seen_, numbers.Integral):
        jxxnd__ine = False
    else:
        jxxnd__ine = True
        m.n_samples_seen_ = np.repeat(m.n_samples_seen_, X.shape[1]).astype(np
            .int64, copy=False)
    dsvf__afjsf = np.zeros((pnmdu__soh, *m.n_samples_seen_.shape), dtype=m.
        n_samples_seen_.dtype)
    rpgm__mqek.Allgather(m.n_samples_seen_, dsvf__afjsf)
    qtg__nplso = np.sum(dsvf__afjsf, axis=0)
    m.n_samples_seen_ = qtg__nplso
    if m.with_mean or m.with_std:
        xxkk__ydebe = np.zeros((pnmdu__soh, *m.mean_.shape), dtype=m.mean_.
            dtype)
        rpgm__mqek.Allgather(m.mean_, xxkk__ydebe)
        xxkk__ydebe[np.isnan(xxkk__ydebe)] = 0
        wjbim__uinng = np.average(xxkk__ydebe, axis=0, weights=dsvf__afjsf)
        m.mean_ = wjbim__uinng
    if m.with_std:
        bzacg__qhruh = sklearn_safe_accumulator_op(np.nansum, (X -
            wjbim__uinng) ** 2, axis=0) / qtg__nplso
        xhg__jnu = np.zeros_like(bzacg__qhruh)
        rpgm__mqek.Allreduce(bzacg__qhruh, xhg__jnu, op=MPI.SUM)
        m.var_ = xhg__jnu
        m.scale_ = sklearn_handle_zeros_in_scale(np.sqrt(m.var_))
    jxxnd__ine = rpgm__mqek.allreduce(jxxnd__ine, op=MPI.LAND)
    if jxxnd__ine:
        m.n_samples_seen_ = m.n_samples_seen_[0]
    return m


