def add(num1, num2):
    def opa():
        return num1 + num2
    print(opa())

def multiply(num1, num2):
    def opm():
        return num1 * num2
    print(opm())

def subtract(num1, num2):
    def ops():
        return num1 - num2
    print(ops())

def divide(num1, num2):
    def opd():
        return num1 / num2
    print(opd())